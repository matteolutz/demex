impl FixtureChannel3Attribute {
    pub fn feature_type(&self) -> FixtureChannel3FeatureType {
        match self {
            Self::Dimmer => FixtureChannel3FeatureType::Dimmer,

            Self::Pan | Self::Tilt => FixtureChannel3FeatureType::PanTilt,

            Self::ColorAddR | Self::ColorAddG | Self::ColorAddB => FixtureChannel3FeatureType::Rgb,

            variant => todo!(
                "impl feature_type() for FixtureChannel3Attribute variant {:?}",
                variant
            ),
        }
    }

    pub fn activation_group(&self) -> Option<FixtureChannel3ActivationGroup> {
        match self {
            Self::Pan | Self::Tilt => Some(FixtureChannel3ActivationGroup::PanTilt),

            Self::ColorAddR | Self::ColorAddG | Self::ColorAddB => {
                Some(FixtureChannel3ActivationGroup::ColorRgb)
            }

            variant => todo!(
                "impl activation_group() for FixtureChannel3Attribute variant {:?}",
                variant
            ),
        }
    }
}

impl ToString for FixtureChannel3Attribute {
    fn to_string(&self) -> String {
        match self {
            Self::Dimmer => "Dimmer".to_string(),

            Self::Pan => "Pan".to_string(),
            Self::Tilt => "Tilt".to_string(),

            Self::ColorAddR => "ColorAdd_R".to_string(),
            Self::ColorAddG => "ColorAdd_G".to_string(),
            Self::ColorAddB => "ColorAdd_B".to_string(),

            variant => todo!(
                "impl ToString for FixtureChannel3Attribute variant {:?}",
                variant
            ),
        }
    }
}
